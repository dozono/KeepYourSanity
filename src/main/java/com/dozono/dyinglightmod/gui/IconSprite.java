package com.dozono.dyinglightmod.gui;

import net.minecraft.util.ResourceLocation;

public class IconSprite {
    public final int u;
    public final int v;
    public final int w;
    public final int h;
    public final ResourceLocation location;

    public IconSprite(int u, int v, int w, int h, ResourceLocation location) {
        this.u = u;
        this.v = v;
        this.w = w;
        this.h = h;
        this.location = location;
    }
}
